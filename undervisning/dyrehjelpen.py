print ("Hei! Skriv inn et dyr:")
dyr = input()

if dyr == "hund":
    print ("Voff! En hund er en lojal venn!")

elif dyr == "katt":
    print ("Mjau! En katt liker å sove og jakte!")

elif dyr == "fugl":
    print ("Kvitre! Fugler kan fly høyt på himmelen!")

elif dyr == "fisk":
    print ("Plask! Fisker svømmer i vannet!")

else:
    print ("Jeg kjenner ikke dette dyret.")